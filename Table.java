package Kursovaya;

public class Table {
    String year, num;

    public Table(String year, String num) {
        this.year = year;
        this.num = num;
    }
    public String getYear() {
        return year;
    }
    public void setYear(String year) {
        this.year = year;
    }
    public String getNum() {
        return num;
    }
    public void setNum(String num) {
        this.num = num;
    }
}
